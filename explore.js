let currentIndex = 0;

const galleryImage = document.getElementById('galleryImage');
const prevButton = document.getElementById('prevButton');
const nextButton = document.getElementById('nextButton');
const stationButton = document.getElementById('stationButton');
const stationPopup = document.getElementById('stationPopup');
const stationButtons = stationPopup.querySelector('.station-buttons');
const stationPrayer = document.getElementById('stationPrayer');
const prayerPopup = document.getElementById('prayerPopup');

function updateImage() {
    galleryImage.src = images[currentIndex];
    stationButton.textContent = `Station ${currentIndex + 1}`;
    updatePrayerContent();
}
function updatePrayerContent() {
    const englishContent = stationContentsEnglish[currentIndex] || { title: '', prayer: '' };
    const hiligaynonContent = stationContentsHiligaynon[currentIndex] || { title: '', prayer: '' };

    document.getElementById('stationTitleEnglish').textContent = englishContent.title;
    document.getElementById('stationContentEnglish').innerHTML = englishContent.prayer;

    document.getElementById('stationTitleHiligaynon').textContent = hiligaynonContent.title;
    document.getElementById('stationContentHiligaynon').innerHTML = hiligaynonContent.prayer;
}



function changeStation(direction) {
    currentIndex = (currentIndex + direction + images.length) % images.length;
    updateImage();
}

function togglePopup(popup) {
    popup.style.display = popup.style.display === 'none' ? 'flex' : 'none';
}

function createStationButtons() {
    for (let i = 0; i < images.length; i++) {
        const button = document.createElement('button');
        button.textContent = `Station ${i + 1}`;
        button.className = 'station-button';
        button.addEventListener('click', () => {
            currentIndex = i;
            updateImage();
            togglePopup(stationPopup);
        });
        stationButtons.appendChild(button);
    }
}


prevButton.addEventListener('click', () => changeStation(-1));
nextButton.addEventListener('click', () => changeStation(1));
stationButton.addEventListener('click', () => togglePopup(stationPopup));
stationPrayer.addEventListener('click', () => {
    updatePrayerContent();
    togglePopup(prayerPopup);
});

stationPopup.addEventListener('click', (e) => {
    if (e.target === stationPopup) togglePopup(stationPopup);
});

prayerPopup.addEventListener('click', (e) => {
    if (e.target === prayerPopup) togglePopup(prayerPopup);
});

createStationButtons();
updateImage();
